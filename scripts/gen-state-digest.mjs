#!/usr/bin/env node
/*
 * P4-7 · STATE digest generator.
 *
 * Regenerates two docs from live Neon, on a hard size budget:
 *   docs/STATE.md         — live Neon counts, last runs, open flags  (HARD CAP < 8 KB)
 *   docs/CONTEXT-PACK.md  — a compact machine/agent context pack
 *
 * Read-only. Every query is wrapped: a missing table or column yields "n/a", never a crash,
 * so the digest survives schema drift. Connection string from NEON_URL (|| NEON_DATABASE_URL
 * || NEON_CONNECTION_STRING). With no key the script writes a clearly-marked "unconfigured"
 * digest and exits 0 (so the workflow is a safe no-op without the secret).
 *
 * Uses the Neon HTTP /sql endpoint (same pattern as functions/api/_neon.js) — no driver dep.
 */
import { writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const STATE_PATH = resolve(ROOT, 'docs/STATE.md');
const PACK_PATH = resolve(ROOT, 'docs/CONTEXT-PACK.md');
const STATE_BUDGET = 8 * 1024; // < 8 KB hard cap for STATE.md

const NEON_URL =
  process.env.NEON_URL || process.env.NEON_DATABASE_URL || process.env.NEON_CONNECTION_STRING || '';

function nowIso() {
  return new Date().toISOString().replace('T', ' ').replace(/\.\d+Z$/, 'Z');
}

async function q(query, params = []) {
  if (!NEON_URL) return { ok: false, rows: [], error: 'neon_unconfigured' };
  const host = NEON_URL.replace(/.*@([^/]+)\/.*/, '$1');
  try {
    const r = await fetch('https://' + host + '/sql', {
      method: 'POST',
      headers: { 'Neon-Connection-String': NEON_URL, 'Content-Type': 'application/json' },
      body: JSON.stringify({ query, params }),
    });
    if (!r.ok) return { ok: false, rows: [], error: 'http_' + r.status };
    const d = await r.json();
    return { ok: true, rows: d.rows || d.results || [], error: null };
  } catch (e) {
    return { ok: false, rows: [], error: (e && e.message) || 'exception' };
  }
}

// count(*) for a table; returns a number or null.
async function count(table, where) {
  const sql = `select count(*)::int as n from ${table}` + (where ? ` where ${where}` : '');
  const { ok, rows } = await q(sql);
  return ok && rows[0] ? Number(rows[0].n) : null;
}

const fmt = (n) => (n === null || n === undefined ? 'n/a' : Number(n).toLocaleString('en-GB'));

async function main() {
  const generated = nowIso();

  if (!NEON_URL) {
    const stub =
      `# Tamazia · STATE (live digest)\n\n` +
      `_Generated ${generated} · NEON_URL not configured — counts unavailable in this run._\n\n` +
      `This file is regenerated by \`.github/workflows/state-digest.yml\` on push to main.\n` +
      `Set the \`NEON_URL\` repository secret to populate live counts.\n`;
    mkdirSync(dirname(STATE_PATH), { recursive: true });
    writeFileSync(STATE_PATH, stub);
    writeFileSync(
      PACK_PATH,
      `# Tamazia · CONTEXT PACK\n\n_Generated ${generated} · NEON_URL unconfigured._\n\n` +
        `Source of truth: GitHub main. Live state: docs/STATE.md (regenerated by state-digest.yml).\n`
    );
    console.log('NEON_URL unset — wrote unconfigured digest. Exit 0.');
    return;
  }

  // ---- Live counts (each defensive) ----
  const leads = await count('leads');
  const leadsReady = await count('leads', "status = 'ready'");
  const leadsQualified = await count('leads', "stage = 'qualified'");
  const leadsClient = await count('leads', "stage = 'client'");
  const sends = await count('sends');
  const sendsSent = await count('sends', "status = 'sent'");
  const suppression = await count('suppression');
  const inbound = await count('inbound_emails');
  const mintQueue = await count('minting_queue');
  const auditPages = await count('audit_pages');
  const calBookings = await count('cal_bookings');

  // ---- Last engine runs (defensive; table may be engine_runs or sourcing_runs) ----
  let lastRuns = [];
  for (const tbl of ['engine_runs', 'sourcing_runs']) {
    const cols = await q(
      `select * from ${tbl} order by 1 desc limit 0` // probe existence cheaply
    );
    if (!cols.ok) continue;
    // Try a few common timestamp columns.
    for (const tcol of ['created_at', 'started_at', 'run_at', 'ts', 'finished_at']) {
      const r = await q(
        `select * from ${tbl} order by ${tcol} desc nulls last limit 5`
      );
      if (r.ok && r.rows.length) {
        lastRuns.push({ table: tbl, tcol, rows: r.rows });
        break;
      }
    }
    if (lastRuns.length) break;
  }

  // ---- Open flags (system_state key/value, if present) ----
  let flags = [];
  const fr = await q(`select key, value from system_state order by key limit 30`);
  if (fr.ok) flags = fr.rows;

  // ---- Build STATE.md (budgeted) ----
  let s = '';
  s += `# Tamazia · STATE (live digest)\n\n`;
  s += `_Generated ${generated} from Neon (read-only). Source of truth = GitHub main. Regenerated by \`.github/workflows/state-digest.yml\`._\n\n`;
  s += `## Live counts\n\n`;
  s += `| Metric | Value |\n|---|---|\n`;
  s += `| leads (total) | ${fmt(leads)} |\n`;
  s += `| leads · email-ready | ${fmt(leadsReady)} |\n`;
  s += `| leads · qualified | ${fmt(leadsQualified)} |\n`;
  s += `| leads · client | ${fmt(leadsClient)} |\n`;
  s += `| sends (total) | ${fmt(sends)} |\n`;
  s += `| sends · sent | ${fmt(sendsSent)} |\n`;
  s += `| suppression | ${fmt(suppression)} |\n`;
  s += `| inbound_emails | ${fmt(inbound)} |\n`;
  s += `| minting_queue | ${fmt(mintQueue)} |\n`;
  s += `| audit_pages | ${fmt(auditPages)} |\n`;
  s += `| cal_bookings | ${fmt(calBookings)} |\n\n`;

  s += `## Last runs\n\n`;
  if (lastRuns.length) {
    const { table, tcol, rows } = lastRuns[0];
    s += `Source: \`${table}\` (ordered by \`${tcol}\`).\n\n`;
    for (const row of rows.slice(0, 5)) {
      const when = row[tcol] || row.created_at || '?';
      const label = row.name || row.kind || row.type || row.source || row.status || row.id || '';
      s += `- ${String(when)} · ${String(label).slice(0, 60)}\n`;
    }
    s += `\n`;
  } else {
    s += `_No engine-run table reachable (engine_runs / sourcing_runs). n/a._\n\n`;
  }

  s += `## Open flags\n\n`;
  if (flags.length) {
    for (const f of flags) {
      s += `- \`${String(f.key).slice(0, 40)}\` = ${String(f.value).slice(0, 60)}\n`;
    }
    s += `\n`;
  } else {
    s += `_No \`system_state\` rows reachable. n/a._\n\n`;
  }

  // Budget enforcement: if over 8 KB, trim trailing sections deterministically.
  const footer = `---\n_Read-only digest. Do not edit by hand; this file is overwritten on every main push._\n`;
  s += footer;
  if (Buffer.byteLength(s, 'utf8') > STATE_BUDGET) {
    // Drop the "Last runs" detail lines first, then flags, keeping headers.
    s = s.replace(/## Last runs[\s\S]*?(?=## Open flags)/, '## Last runs\n\n_Trimmed for size budget._\n\n');
    if (Buffer.byteLength(s, 'utf8') > STATE_BUDGET) {
      s = s.replace(/## Open flags[\s\S]*?(?=---\n)/, '## Open flags\n\n_Trimmed for size budget._\n\n');
    }
    // Last resort: hard truncate to the budget on a line boundary.
    if (Buffer.byteLength(s, 'utf8') > STATE_BUDGET) {
      const buf = Buffer.from(s, 'utf8').subarray(0, STATE_BUDGET - footer.length - 64);
      s = buf.toString('utf8').replace(/\n[^\n]*$/, '\n') + '\n' + footer;
    }
  }

  mkdirSync(dirname(STATE_PATH), { recursive: true });
  writeFileSync(STATE_PATH, s);
  const bytes = Buffer.byteLength(s, 'utf8');
  console.log(`Wrote docs/STATE.md (${bytes} bytes, budget ${STATE_BUDGET}).`);
  if (bytes > STATE_BUDGET) {
    console.error('STATE.md exceeds budget after trimming.');
    process.exitCode = 1;
  }

  // ---- Build CONTEXT-PACK.md (compact agent context) ----
  let p = '';
  p += `# Tamazia · CONTEXT PACK\n\n`;
  p += `_Generated ${generated}. Compact context for agents. Source of truth = GitHub main; live state = docs/STATE.md._\n\n`;
  p += `## What this is\n`;
  p += `Tamazia = compliance-led SEO/GEO agency. Two systems share one Neon DB: the AGENCY pipeline and the AUDIT ENGINE (off-limits).\n\n`;
  p += `## Live snapshot\n`;
  p += `- leads ${fmt(leads)} (ready ${fmt(leadsReady)} · qualified ${fmt(leadsQualified)} · client ${fmt(leadsClient)})\n`;
  p += `- sends ${fmt(sends)} (sent ${fmt(sendsSent)}) · suppression ${fmt(suppression)} · inbound ${fmt(inbound)}\n`;
  p += `- minting_queue ${fmt(mintQueue)} · audit_pages ${fmt(auditPages)} · cal_bookings ${fmt(calBookings)}\n\n`;
  p += `## Pipeline (one line)\n`;
  p += `SOURCE → ENRICH+extract → VERIFY → QUALIFY+ICP+GOVERNOR → MINT audit → SEND → REPLY/booking or recycle → nightly RECONCILE → Neon → Cockpit.\n\n`;
  p += `## Open flags\n`;
  if (flags.length) {
    for (const f of flags.slice(0, 20)) p += `- ${String(f.key).slice(0, 40)} = ${String(f.value).slice(0, 50)}\n`;
  } else {
    p += `- n/a\n`;
  }
  p += `\n---\n_Read-only digest. Regenerated on every main push by state-digest.yml._\n`;
  writeFileSync(PACK_PATH, p);
  console.log(`Wrote docs/CONTEXT-PACK.md (${Buffer.byteLength(p, 'utf8')} bytes).`);
}

main().catch((e) => {
  console.error('digest failed:', e && e.message);
  // Do not hard-fail the workflow on a transient Neon issue; leave existing docs in place.
  process.exitCode = 0;
});
